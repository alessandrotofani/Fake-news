from turtle import *


class Pen(Turtle):
    def __init__(self, x, y):
        Turtle.__init__(self)
        self.pendown()
        self.setx(x)
        self.sety(y)
        print('pen created')

    def black(self, x, y):
        self.setx(x)
        self.sety(y)

    def white(self, x, y):
        self.color("white")
        self.setx(x)
        self.sety(y)
        self.color("black")

    def label(self, s):
        self.write(s, font=("Arial", 8))
